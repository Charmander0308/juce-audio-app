void MainComponent::getNextAudioBlock(const juce::AudioSourceChannelInfo& bufferToFill)
{
    // 입력 받은 오디오 데이터의 크기(RMS 레벨)를 계산합니다.
    auto level = bufferToFill.buffer->getRMSLevel(0, bufferToFill.startSample, bufferToFill.numSamples);

    // 소리가 일정 크기 이상 들어오면 출력 창에 디버그 메시지를 찍습니다.
    if (level > 0.01f)
        juce::Logger::writeToLog("입력 신호 감지됨! 레벨: " + juce::String(level));

    // 스피커로 나가는 소리는 일단 뮤트 처리합니다 (하울링 방지)
    bufferToFill.clearActiveBufferRegion();
}

